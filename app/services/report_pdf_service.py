"""
Report PDF Generation Service.

Generates PDF reports containing data tables and chart images using fpdf2.
Used when the AI generates charts/tables and the user wants them sent via email.
"""

import io
import re
from datetime import datetime
from fpdf import FPDF


def _sanitize_for_pdf(text: str) -> str:
    """Removes emojis and non-latin1 characters from text for safe PDF rendering with Helvetica."""
    if not text:
        return ""
    # Remove emoji and other non-BMP characters (above U+FFFF)
    text = re.sub(r'[\U00010000-\U0010FFFF]', '', text)
    # Remove remaining characters outside latin-1 range (above U+00FF)
    # but keep common accented chars (U+00C0 to U+00FF)
    result = []
    for ch in text:
        if ord(ch) <= 0xFF:
            result.append(ch)
        elif ch in '\u2013\u2014\u2018\u2019\u201c\u201d':
            # Replace common typographic chars with ASCII equivalents
            replacements = {'\u2013': '-', '\u2014': '-', '\u2018': "'", '\u2019': "'", '\u201c': '"', '\u201d': '"'}
            result.append(replacements.get(ch, ''))
        else:
            # Skip the character
            result.append('')
    return ''.join(result).strip()


